struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode *left, TreeNode *right)
      : val(x), left(left), right(right) {}
};
#include <bits/stdc++.h>
using namespace std;
// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
 public:
  bool eq(TreeNode *root, TreeNode *root2) {
    if (root == nullptr && root2 != nullptr)
      return false;
    else if (root != nullptr && root2 == nullptr)
      return false;
    else
      return (root == nullptr && root2 == nullptr) ||
             (root->val == root2->val && eq(root->left, root2->right) &&
              eq(root->right, root2->left));
  }
  bool isSymmetric(TreeNode *root) {
    if (root->left == nullptr && root->right == nullptr)
      return true;
    else if (root->left == nullptr && root->right != nullptr)
      return false;
    else if (root->left != nullptr && root->right == nullptr)
      return false;

    return eq(root->left, root->right);
  }
};
