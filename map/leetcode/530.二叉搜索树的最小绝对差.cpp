struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode *left, TreeNode *right)
      : val(x), left(left), right(right) {}
};
#include <bits/stdc++.h>
using namespace std;
// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
 public:
  int get(TreeNode *node) {
    int min_diff = INT_MAX;
    if (!node) return min_diff;
    TreeNode *cur;
    if (node->left) {
      cur = node->left;
      while (cur->right) cur = cur->right;
      min_diff = min(min_diff, node->val - cur->val);
    }
    if (node->right) {
      cur = node->right;
      while (cur->left) cur = cur->left;
      min_diff = min(min_diff, cur->val - node->val);
    }
    if (node->left) min_diff = min(min_diff, get(node->left));
    if (node->right) min_diff = min(min_diff, get(node->right));
    return min_diff;
  }
  int getMinimumDifference(TreeNode *root) { return get(root); }
};
// @lc code=end
